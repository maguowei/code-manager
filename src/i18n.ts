export type TranslationKey = keyof typeof translations.zh;

// 设置持久化
const STORAGE_KEY = "code-manager-settings";

interface AppSettings {
  language: Language;
}

function isChineseSystemLocale(locale: string | undefined): boolean {
  return (locale ?? "").trim().replace("_", "-").toLowerCase().startsWith("zh");
}

function getSystemLanguage(): Language {
  if (typeof navigator === "undefined") {
    return "en";
  }

  const primaryLanguage =
    Array.isArray(navigator.languages) && navigator.languages.length > 0
      ? navigator.languages.find((language) => language.trim() !== "")
      : navigator.language;

  return isChineseSystemLocale(primaryLanguage) ? "zh" : "en";
}

function loadSettings(): AppSettings {
  const defaults: AppSettings = { language: getSystemLanguage() };
  try {
    const stored = localStorage.getItem(STORAGE_KEY);
    if (stored) {
      const parsed = JSON.parse(stored);
      // 校验解析结果，防止 localStorage 数据损坏导致崩溃
      if (parsed && typeof parsed === "object") {
        const validLanguages: Language[] = ["zh", "en"];
        return {
          language: validLanguages.includes(parsed.language) ? parsed.language : defaults.language,
        };
      }
    }
  } catch {
    // 忽略解析错误
  }
  return defaults;
}

function saveSettings(settings: AppSettings) {
  localStorage.setItem(STORAGE_KEY, JSON.stringify(settings));
}

// Context
interface I18nContextType {
  language: Language;
  t: (key: TranslationKey) => string;
  setLanguage: (lang: Language) => void;
}

const I18nContext = createContext<I18nContextType | null>(null);

// Provider 组件
export function I18nProvider({ children }: { children: ReactNode }) {
  const [settings, setSettings] = useState<AppSettings>(loadSettings);

  const t = useCallback(
    (key: TranslationKey): string => {
      return translations[settings.language][key] || key;
    },
    [settings.language],
  );

  const setLanguage = useCallback((language: Language) => {
    setSettings((prev) => {
      const next = { ...prev, language };
      saveSettings(next);
      return next;
    });
  }, []);

  useEffect(() => {
    if (!isTauri()) return;

    let cancelled = false;

    void ipc
      .getConfigWorkspace()
      .then((workspace) => {
        if (cancelled) return;

        const backendLanguage: Language = workspace.app.uiLanguage === "en" ? "en" : "zh";
        setSettings((prev) => {
          if (prev.language === backendLanguage) {
            return prev;
          }
          const next = { ...prev, language: backendLanguage };
          saveSettings(next);
          return next;
        });
      })
      .catch(() => {
        // 忽略初始化同步失败，回退到本地缓存语言
      });

    return () => {
      cancelled = true;
    };
  }, []);

  const value = useMemo<I18nContextType>(
    () => ({
      language: settings.language,
      t,
      setLanguage,
    }),
    [settings.language, t, setLanguage],
  );

  return createElement(I18nContext.Provider, { value }, children);
}

// Hook
export function useI18n(): I18nContextType {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used within an I18nProvider");
  }
  return context;
}
